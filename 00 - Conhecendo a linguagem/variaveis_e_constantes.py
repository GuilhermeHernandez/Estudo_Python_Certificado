'''

Oque são ?

    Variaveis 
        - Armazena valores
        - O valor pode se alterar no meio do percuso
        - Não precisamos definir o tipo da variavel (tipagem dinamica)

        - Ex : age -> 23
        - Ex : name -> 'Guilherme'

    Constantes -> Declaradas com letras maiusculas
        - Armazena valor
        - Nasce com um valor e não pode ser alterado

        - Ex = PASTA_RAIZ = '/home/guilherme/Documents/Python'
        - Ex = ESTADOS = [
            'SP',
            'RJ',
            'MJ',
        ]


Boas Praticas 

    - todo espaço -> '_'
    - nomes sugestivos
    - nome de constantes tudo em maiusculo
    
'''

