'''

Operadores de comparação 
    - ver se variaveis são igual, diferente , maior , menor entre outras
    - (== , != , > ou >= , < ou <=)

'''

saldo = 200
saque = 200

print(saldo == saque)
print(saldo != saque)
print(saldo > saque)
print(saldo >= saque)
print(saldo < saque)
print(saldo <= saque)