'''

Conhecendo operadores aritmeticos 
    -   Executa função matematicas
    -  ( + , - , * , / , // -> Div inteira , % , ** )
    -   Precendencia definida na metematica padrão 

'''

produto_1 = 10 
produto_2 = 20

print(produto_1 + produto_2 + 3.5)

print(produto_1 - produto_2 - 3.5)

print(produto_1 / produto_2)

print(produto_1 // produto_2)

print(produto_1 * produto_2)

print(produto_1 % produto_2)

print(produto_1 ** produto_2)

x = 10 + 5 * 4

print(x)

x = (10 + 5) * 4

print(x)